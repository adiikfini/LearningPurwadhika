/* Comparison operator
    >, <, >=, <=, ==, ===, !=, !==
*/

console.log(3 > 2); // true
console.log(3 < 2); // false
console.log(3 >= 2); // true
console.log(3 <= 2); // false
console.log(3 == 2); // false
console.log(3 != 2); // true

console.log("3" == 3); // true, karena hanya membandingkan nilai
console.log("3" === 3); // false, karena membandingkan nilai dan tipe data